# Exemplo lista

# lista vazia
listasVazia = []
print(listasVazia)

lista = [3, 60, 10, 45, 20]
print(lista)

# acessar um item
print(lista[2])
print(lista[0])

# inserir um item no final da lista
lista.append(100)
print(lista)
lista.append(300)
print(lista)

# remover um item
lista.pop(0)
print(lista)

# alterar um item
lista[0] = 99
print(lista)